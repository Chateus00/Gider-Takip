function getByPath(tree: TranslationTree, path: string) {
  return path.split(".").reduce<TranslationLeaf | TranslationTree | undefined>((current, segment) => {
    if (!current || typeof current === "string" || Array.isArray(current)) {
      return undefined;
    }

    return current[segment];
  }, tree);
}

function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, token) => String(values[token] ?? ""));
}

export function getStoredLanguage(): Language {
  if (typeof window === "undefined") {
    return defaultLanguage;
  }

  const value = window.localStorage.getItem(languageStorageKey) as Language | null;
  return value && value in languageMeta ? value : defaultLanguage;
}

export function translate(
  key: string,
  values?: Record<string, string | number>,
  language: Language = getStoredLanguage()
) {
  const languageTree = translations[language] ?? translations[defaultLanguage];
  const fallbackTree = translations[defaultLanguage];
  const resolved = getByPath(languageTree, key) ?? getByPath(fallbackTree, key);

  if (Array.isArray(resolved)) {
    return resolved.map((item) => interpolate(item, values));
  }

  if (typeof resolved === "string") {
    return interpolate(resolved, values);
  }

  return key;
}

export function translateText(
  key: string,
  values?: Record<string, string | number>,
  language: Language = getStoredLanguage()
) {
  const result = translate(key, values, language);
  return Array.isArray(result) ? result.join(", ") : result;
}
